<h1 class="tituvac">3 perguntas frequentes sobre as vacinas</h1>

<h5 class="pvac">1- Quanto tempo devo esperar para tomar a próxima dose da vacina?</h5>
<p class="pvac">Caso já tenha tomado a primeira dose, para tomar a segunda, o Governo do Estado de São Paulo estipulou um intervlo de 3 meses. Já para tomar a terceira dose, foi estipulado um intervalo de 5 meses. </p>

<h5 class="pvac">2- As vacinas são efetivas contra a variante Delta?</h5>
<p class="pvac">Estudos demonstraram que as vacinas aprovadas, sejam elas aprovadas pela OMS ou por uma agência reguladora nacional rigorosa, oferecem excelente proteção (em termos de doença grave e hospitalização) contra a variante Delta e outras variantes de preocupação.
</p>

<h5 class="pvac">3- Por que algumas pessoas vacinadas ficam doentes mesmo assim?</h5>
<p class="pvac">As vacinas são muito efetivas e são uma ferramenta fundamental para controlar a pandemia,porém nenhuma vacina é 100% efetiva para prevenir a doença em pessoas vacinadas.</p>


